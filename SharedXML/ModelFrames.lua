function GetUICameraInfo(uiCameraID)
	local uiCamera = UI_CAMERA_ID[uiCameraID];
	if uiCamera then
		return uiCamera[1], uiCamera[2], uiCamera[3], uiCamera[4], uiCamera[5];
	else
		return 0, 0, 0, 0, 0;
	end
end

local cameraWidth = 1366;
local cameraHeight = 768;
local cameraSquare = math.sqrt(cameraWidth * cameraWidth + cameraHeight * cameraHeight);

function Model_ApplyUICamera(self, uiCameraID, posXOffest, posYOffest, posZOffest)
	local posX, posY, posZ, yaw, animId = GetUICameraInfo(uiCameraID);
	if posX and posY and posZ and yaw then
		local scale = self:GetEffectiveScale() - (self:GetEffectiveScale() - UIParent:GetScale());
		local width = GetScreenWidth() * scale;
		local height = GetScreenHeight() * scale;

		local square = math.sqrt(width * width + height * height);
		local diff = (cameraSquare / square) * self:GetModelScale();

		if posXOffest and posXOffest ~= 1 then posX = posX * posXOffest end
		if posYOffest and posYOffest ~= 1 then posY = posY * posYOffest end
		if posZOffest and posZOffest ~= 1 then posZ = posZ * posZOffest end

		self:SetPosition(posX * diff, posY * diff, posZ * diff);
		self:SetFacing(yaw);

		if animId then
			self:SetSequence(animId);
		else
			self:SetSequence(3);
		end

		self.animId = animId;
	end
end

function GetUICameraIDByType(cameraType, unit)
	if unit then
		if not UnitExists(unit) then
			return
		end
	else
		unit = "player"
	end

	local _, race, raceID = UnitRace(unit)
	local sexID = UnitSex(unit)
	local sex = sexID == 3 and "Female" or "Male"

	local cameraInfo = UI_CAMERA[race] and UI_CAMERA[race][sex]
	if not cameraInfo then
		cameraInfo = UI_CAMERA["Human"]["Male"]
	end

	return cameraInfo[cameraType]
end